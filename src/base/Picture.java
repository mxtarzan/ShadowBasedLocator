public class Picture {
	public double date;
	public double time;
	public double height;
	public double shadowlength;
	// days in each month jan  feb mar apr may jun jul aug sep oct nov dec
	
	public Picture(double d, double t, double h, double sl) {
		date = d;
		time = t;
		height = h;
		shadowlength = sl;
	}
}
